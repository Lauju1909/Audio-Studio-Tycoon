import sys

def get_system_language():
    """Detect default OS language, return 'de' or 'en'."""
    try:
        if sys.platform == "win32":
            import ctypes
            windll = ctypes.windll.kernel32
            lang_id = windll.GetUserDefaultUILanguage()
            # Primary language ID is the lower 10 bits
            primary_lang = lang_id & 0x3ff
            # 0x07 is German, 0x09 is English
            if primary_lang == 0x07:
                return 'de'
        return 'en'
    except Exception:
        return 'en'

CURRENT_LANGUAGE = get_system_language()

def set_language(lang):
    global CURRENT_LANGUAGE
    CURRENT_LANGUAGE = lang

def get_text(key, **kwargs):
    text = TRANSLATIONS.get(CURRENT_LANGUAGE, TRANSLATIONS['en']).get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text

